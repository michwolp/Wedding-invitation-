export function getMessage(lang, key, guestForm) {
  if (lang === 'he' && guestForm && MSG_HE_SINGULAR[guestForm]?.[key] != null) {
    return MSG_HE_SINGULAR[guestForm][key];
  }
  return MESSAGES[lang]?.[key] ?? MESSAGES.he[key];
}

export function getErrorMessage(lang, code) {
  const table = ERROR_MESSAGES[lang] || ERROR_MESSAGES.he;
  return table[code] != null ? table[code] : table[''];
}

export function getGreeting(lang, name) {
  const templates = {
    he: n => `שלום ${n} 🤍`,
    en: n => `Hello ${n} 🤍`,
    ru: n => `Привет, ${n} 🤍`,
  };
  return (templates[lang] || templates.he)(name);
}
